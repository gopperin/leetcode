package array

import (
	"fmt"
)

type question37 struct {
	para37
	ans37
}

// para 是参数
// one 代表第一个参数
type para37 struct {
	s [][]byte
}

// ans 是答案
// one 代表第一个答案
type ans37 struct {
	s [][]byte
}

func ExampleSolveSudoku() {

	qs := []question37{

		{
			para37{[][]byte{
				{'5', '3', '.', '.', '7', '.', '.', '.', '.'},
				{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
				{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
				{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
				{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
				{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
				{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
				{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
				{'.', '.', '.', '.', '8', '.', '.', '7', '9'}}},
			ans37{[][]byte{
				{'5', '3', '4', '6', '7', '8', '9', '1', '2'},
				{'6', '7', '2', '1', '9', '5', '3', '4', '8'},
				{'1', '9', '8', '3', '4', '2', '5', '6', '7'},
				{'8', '5', '9', '7', '6', '1', '4', '2', '3'},
				{'4', '2', '6', '8', '5', '3', '7', '9', '1'},
				{'7', '1', '3', '9', '2', '4', '8', '5', '6'},
				{'9', '6', '1', '5', '3', '7', '2', '8', '4'},
				{'2', '8', '7', '4', '1', '9', '6', '3', '5'},
				{'3', '4', '5', '2', '8', '6', '1', '7', '9'}}},
		},
	}

	for _, q := range qs {
		_, p := q.ans37, q.para37
		SolveSudoku(p.s)
		fmt.Println(p)
	}
	// output:
	// {[[53 51 52 54 55 56 57 49 50] [54 55 50 49 57 53 51 52 56] [49 57 56 51 52 50 53 54 55] [56 53 57 55 54 49 52 50 51] [52 50 54 56 53 51 55 57 49] [55 49 51 57 50 52 56 53 54] [57 54 49 53 51 55 50 56 52] [50 56 55 52 49 57 54 51 53] [51 52 53 50 56 54 49 55 57]]}
}
